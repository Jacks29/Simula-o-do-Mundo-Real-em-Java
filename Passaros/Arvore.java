public class Arvore {
    private String especie;
    private int idade;
    private String tamanho;
    private double forcaDoGalio;
    private String vazia;

    /**
     * @return the especie
     */
    public String getEspecie() {
        return especie;
    }

    /**
     * @param especie the especie to set
     */
    public void setEspecie(String especie) {
        this.especie = especie;
    }

    /**
     * @return the idade
     */
    public int getIdade() {
        return idade;
    }

    /**
     * @param idade the idade to set
     */
    public void setIdade(int idade) {
        this.idade = idade;
    }

    /**
     * @return the tamanho
     */
    public String getTamanho() {
        return tamanho;
    }

    /**
     * @param tamanho the tamanho to set
     */
    public void setTamanho(String tamanho) {
        this.tamanho = tamanho;
    }

    /**
     * @return the forcaDoGalio
     */
    public double getForcaDoGalio() {
        return forcaDoGalio;
    }

    /**
     * @param forcaDoGalio the forcaDoGalio to set
     */
    public void setForcaDoGalio(double forcaDoGalio) {
        this.forcaDoGalio = forcaDoGalio;
    }

    /**
     * @return the vazia
     */
    public String getVazia() {
        return vazia;
    }

    /**
     * @param vazia the vazia to set
     */
    public void setVazia(String vazia) {
        this.vazia = vazia;
    }
}
