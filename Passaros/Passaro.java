public class Passaro {
    private String nome;
    private String faseDaVida;
    private double peso;
    private String porte;
    private String estado;

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the faseDaVida
     */
    public String getFaseDaVida() {
        return faseDaVida;
    }

    /**
     * @param faseDaVida the faseDaVida to set
     */
    public void setFaseDaVida(String faseDaVida) {
        this.faseDaVida = faseDaVida;
    }

    /**
     * @return the peso
     */
    public double getPeso() {
        return peso;
    }

    /**
     * @param peso the peso to set
     */
    public void setPeso(double peso) {
        this.peso = peso;
    }

    /**
     * @return the porte
     */
    public String getPorte() {
        return porte;
    }

    /**
     * @param porte the porte to set
     */
    public void setPorte(String porte) {
        this.porte = porte;
    }

    /**
     * @return the estado
     */
    public String getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(String estado) {
        this.estado = estado;
    }
}
